while True:
    name = input()
    if name == "exit":
        break 
    else:
        print("Hi, ", name)
        print("TRY AGAIN")

#break - операторное слово, упоминающиеся только внутри цикла
# - ОПЕРАТОР досрочного завершения цикла (остановка итерации цикла + выход из цикла)

#continue - операторное слово, тоже только внутри цикла
# - ОПЕРАТОР досрочного завершения итерации (остановка итерации цикла + переход к следующей итерации)